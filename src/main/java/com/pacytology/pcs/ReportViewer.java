	class SymKey extends java.awt.event.KeyAdapter
	{
		public void keyPressed(java.awt.event.KeyEvent event)
		{
			Object object = event.getSource();
			if (object == ReportViewer.this)
				ReportViewer_keyPressed(event);
		}
	}

	void ReportViewer_keyPressed(java.awt.event.KeyEvent event)
	{
		if (event.getKeyCode()==event.VK_F9) {
		    this.dispose();
		}
		else if (event.getKeyCode()==event.VK_F1) {
		    if (verifyPrinter()) {
		        this.setCursor(new Cursor(Cursor.WAIT_CURSOR));
		        Utils.genericPrint(dir,fileName,false,printerCodes);
		        this.setCursor(new Cursor(Cursor.DEFAULT_CURSOR));
		    }
		}
	}
	
	boolean verifyPrinter()
	{
	    boolean status = false;
		int rv = printerConfirm.showConfirmDialog(this,"Make sure printer is ready. \nPrint report now?",
		    this.getTitle(),printerConfirm.YES_NO_OPTION,printerConfirm.QUESTION_MESSAGE);
		if (rv==printerConfirm.YES_OPTION) {
		    status=true;
		}
		return (status);
	}
	
